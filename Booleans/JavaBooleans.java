package Booleans;

public class JavaBooleans{
    public static void main(String[] args) {
        boolean isJavaFun = true;
        boolean isJavaTasty = false;
        System.out.println(isJavaFun);
        System.out.println(isJavaTasty);

        // boolean expression
        int x = 5;
        int y = 6;
        System.out.println(x>y);
    }
}