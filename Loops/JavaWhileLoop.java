package Loops;

public class JavaWhileLoop{
    public static void main(String[] args){
        int i=0;
        while(i < 5){
            System.out.println(i);
            i++;
        }

        System.out.println(" do while loop hihiih.");

        int j = 0;

        do{
            System.out.println(j);
            j++;
        }while(j<5);
    }
}